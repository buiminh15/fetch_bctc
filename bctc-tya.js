async function fetchAndExtractData() {
  try {
    const response = await axios.get('https://taya.com.vn/quan-he-co-dong/bao-cao-tai-chinh-quy-ad52.html', {
      headers: {
        'accept': 'text/html',
        'user-agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/138.0.0.0 Safari/537.36',
      },
      timeout: 60000
    });

    const html = response.data;
    const $ = cheerio.load(html);
    const currentYear = new Date().getFullYear();
    // L·∫•y t·ªëi ƒëa 5 b√°o c√°o m·ªõi nh·∫•t
    const names = [];
    $('td.name').each((index, element) => {
      if (index < 5) {
        const nameRaw = $(element).text().trim();
        const name = he.decode(nameRaw);
        if (name.includes(`${currentYear}`)) {
          names.push(`${name}`);
        }

      } else {
        return false; // Break the loop
      }
    });
    console.log('üì¢ [bctc-tya.js:48]', names);
    if (names.length === 0) {
      console.log('Kh√¥ng t√¨m th·∫•y b√°o c√°o t√†i ch√≠nh n√†o.');
      return;
    }

    // L·ªçc ra c√°c b√°o c√°o ch∆∞a c√≥ trong DB
    const newNames = await filterNewNames(names, COMPANIES.TYA);
    console.log('üì¢ [bctc-cdn.js:46]', newNames);
    if (newNames.length) {
      await insertBCTC(newNames, COMPANIES.TYA);

      // G·ª≠i th√¥ng b√°o Telegram cho t·ª´ng b√°o c√°o m·ªõi
      await Promise.all(
        newNames.map(name => {
          return sendTelegramNotification(`B√°o c√°o t√†i ch√≠nh c·ªßa TYA::: ${name}`);
        })
      );
      console.log(`ƒê√£ th√™m ${newNames.length} b√°o c√°o m·ªõi v√† g·ª≠i th√¥ng b√°o.`);
    } else {
      console.log('Kh√¥ng c√≥ b√°o c√°o m·ªõi.');
    }
  } catch (error) {
    console.error('Error fetching HTML:', error);
    process.exit(1);
  }
}

